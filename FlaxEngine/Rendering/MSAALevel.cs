namespace FlaxEngine.Rendering
{
    /// <summary>
    /// Multisample count level.
    /// </summary>
    public enum MSAALevel
    {
        /// <summary>
        /// Disabled multisampling.
        /// </summary>
        None = 1,

        /// <summary>
        /// Two samples per pixel.
        /// </summary>
        X2 = 2,

        /// <summary>
        /// Four samples per pixel.
        /// </summary>
        X4 = 4,

        /// <summary>
        /// Eight samples per pixel.
        /// </summary>
        X8 = 8
    }
}
